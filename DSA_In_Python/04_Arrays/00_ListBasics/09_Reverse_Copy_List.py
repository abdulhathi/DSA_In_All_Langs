# Reverse order print
thislist = ["banana", "Orange", "Kiwi", "cherry"]
thislist.reverse()
print(thislist)

# Copy the list items to a new list
thislist = ["banana", "Orange", "Kiwi", "cherry"]
newList = thislist.copy()
print(newList)

# Copy the list items another way
thislist = ["banana", "Orange", "Kiwi", "cherry"]
newList = list(thislist)
print(newList)