# Print string
print("abdul")

# Assign a string to a variable
s = "Abdul Hathi"
print(s)

# Multiline string using double quotes
para = """Hi Frank, 

    This is Abdul Hathi Mohamed Hussain.
Hi How are you.

Thanks,
Abdul
"""
print(para)

# Multiline sting using single quotes
para = '''Hi Luis,

What are the thing are going on.

Thanks,
Abdul'''
print(para)

# String are Arrays
a = "This is Abdul"
print(a[10])

# Loop and print the chars
for c in "Abdul":
    print(c, end="")
